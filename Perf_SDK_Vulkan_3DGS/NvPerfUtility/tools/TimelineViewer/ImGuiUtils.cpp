    bool BeginMainViewportTopBar(const char* pLabel, float height, ImGuiWindowFlags flags)
    {
        // https://github.com/ocornut/imgui/issues/3518
        ImGuiViewportP* pMainViewport = (ImGuiViewportP*)(void*)ImGui::GetMainViewport();
        return ImGui::BeginViewportSideBar(pLabel, pMainViewport, ImGuiDir_Up, height, flags);
    }

    bool BeginMainViewportToolBar(const char* pLabel, const ImVec2& padding)
    {
        ImGui::PushStyleVar(ImGuiStyleVar_WindowPadding, padding);
        const float height = ImGui::GetFrameHeight() + 2.0f * ImGui::GetStyle().WindowPadding.y;
        const ImGuiWindowFlags flags = ImGuiWindowFlags_NoScrollbar | ImGuiWindowFlags_NoSavedSettings;
        bool isOpen = BeginMainViewportTopBar(pLabel, height, flags);
        ImGui::PopStyleVar();
        return isOpen;
    }

